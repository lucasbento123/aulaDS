#' Soma dois nemeros
#'
#'  Recebe um numero x e outro y e retorna a soma de x e y.
#'
#' @param x primeiro argumento
#' @param y segundo argumento
#'
#' @return a soma de dois numeros
#'
#' @export

soma <- function(x, y){
  x + y
}

soma(1,2)

